export function tr<T extends keyof typeof translations>(
  section: T,
  key: keyof (typeof translations)[T],
  locale: Locale
): string {
  const item = (translations[section] as Record<string, { tr: string; en: string }>)[key as string];
  return item?.[locale] ?? (key as string);
}

/** Dropdown option label - TR value'yu locale'e göre çevirir */
export function trOption(
  type: "sectors" | "budgets" | "services",
  value: string,
  locale: Locale
): string {
  if (locale === "tr") return value;
  const map = translations.contactFormOptions[type] as Record<string, string>;
  return map?.[value] ?? value;
}
